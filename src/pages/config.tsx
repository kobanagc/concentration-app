import Head from 'next/head';
import { useRouter } from 'next/router';
import styles from '@/styles/Config.module.css';
import { useState, useEffect } from 'react';

const Config = () => {
  const [numPlayers, setNumPlayers] = useState(2);
  const [includeJoker, setIncludeJoker] = useState(false);
  const [numPairs, setNumPairs] = useState(20);
  const [playerNames, setPlayerNames] = useState<string[]>(Array(numPlayers).fill(''));
  const [selectedImages, setSelectedImages] = useState<File[]>([]);
  const [uploadError, setUploadError] = useState<string>('');
  const router = useRouter();

  useEffect(() => {
    // ファイルのアップロード数が制限を超えた場合、Start Gameボタンを非活性にする
    if (selectedImages.length !== 0 && selectedImages.length !== getValidUploadCount()) {
      setUploadError(`ファイルをアップロードする場合は ${getValidUploadCount()} 枚をアップロードしてください。`);
    } else {
      setUploadError('');
    }
  }, [selectedImages, includeJoker, numPairs]);

  const handleNumPlayersChange = (event: React.ChangeEvent<HTMLInputElement>) => {
    const value = parseInt(event.target.value);
    setNumPlayers(value);
    setPlayerNames(Array(value).fill(''));
  };

  const handlePlayerNameChange = (index: number, event: React.ChangeEvent<HTMLInputElement>) => {
    const value = event.target.value;
    setPlayerNames((prevNames) => {
      const newNames = [...prevNames];
      newNames[index] = value;
      return newNames;
    });
  };

  const handleImageUpload = (event: React.ChangeEvent<HTMLInputElement>) => {
    const files = event.target.files;
    if (files) {
      const fileList = Array.from(files);
      setSelectedImages(fileList);
    }
  };

  const handleSubmit = (event: React.FormEvent) => {
    event.preventDefault();
    // プレイ画面に遷移する処理を追加
    const formData = {
      numPlayers,
      includeJoker,
      numPairs: selectedImages.length === 0 ? 20 : numPairs,
      playerNames,
      selectedImages,
    };
    localStorage.setItem('gameData', JSON.stringify(formData));
    router.push('/game');
  };

  // ファイルのアップロード数の制限を取得する関数
  const getValidUploadCount = (): number => {
    return includeJoker ? numPairs + 1 : numPairs;
  };

  return (
    <>
      <Head>
        <title>Config</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
      </Head>
      <div className={styles.settings}>
        <h1 className={styles.title}>Settings</h1>
        <form onSubmit={handleSubmit}>
          <label>
            Number of Players:
            <input
              type="number"
              value={numPlayers}
              onChange={handleNumPlayersChange}
              min="2"
              max="10"
              className={styles.input}
            />
          </label>

          <label>
            Include Joker:
            <input
              type="checkbox"
              checked={includeJoker}
              onChange={() => setIncludeJoker(!includeJoker)}
              className={styles.checkbox}
            />
          </label>

          <label>
            Number of Pairs:
            <input
              type="number"
              value={numPairs}
              onChange={(event) => setNumPairs(parseInt(event.target.value))}
              min="10"
              className={styles.input}
            />
          </label>

          <h2 className={styles.subtitle}>Player Names:</h2>
          {playerNames.map((name, index) => (
            <label key={index} className={styles.label}>
              Player {index + 1}:
              <input
                type="text"
                value={name}
                onChange={(event) => handlePlayerNameChange(index, event)}
                required
                className={styles.input}
              />
            </label>
          ))}

          <h2 className={styles.subtitle}>Card Images:</h2>
          <input
            type="file"
            accept="image/*"
            multiple
            onChange={handleImageUpload}
            className={styles.fileInput}
          />

          <button type="submit" className={styles.button} disabled={uploadError !== ''}>
            Start Game
          </button>
          {uploadError && <p className={styles.error}>{uploadError}</p>}
          {selectedImages.length === 0 && (
            <p className={styles.info}>画像がアップロードされなければペア数は20枚として設定されます。</p>
          )}
        </form>
      </div>
    </>
  );
};

export default Config;
